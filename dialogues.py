    def get_dock_log(self, current_tick):
        """
        Генерирует редкие фоновые фразы для дока, чтобы избежать спама.
        Вызывается каждый тик, но выдает текст раз в 6 тиков (24 секунды).
        """
        if current_tick - self.last_dock_tick < 6:
            return None

        self.last_dock_tick = current_tick
        
        role = random.choice(["Капитан", "Второй пилот", "Инженер"])
        phrase = random.choice(self.dock_pool[role])
        return phrase

    def get_flight_log(self):
        """
        Проверяет условия сюжета и уровень стресса экипажа.
        Возвращает сюжетную реплику, если порог стресса достигнут, и накладывает эффекты.
        """
        if self.current_story_step >= len(self.flight_scenario):
            return None

        current_scene = self.flight_scenario[self.current_story_step]
        
        officers = self.crew_manager.get_all_officers()
        max_current_stress = max([o.stress for o in officers]) if officers else 0

        if max_current_stress >= current_scene["stress_trigger"]:
            text_to_return = current_scene["text"]
            
            effects = current_scene["effect"]
            if effects:
                for role_name, val in effects.items():
                    officer = self.crew_manager.get_officer_by_role(role_name)
                    if officer:
                        officer.stress += val
            
            self.current_story_step += 1
            return text_to_return

        return None

